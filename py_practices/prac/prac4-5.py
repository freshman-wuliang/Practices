fruits = ["apple","pear","mango","watermelon"]
print("list:" + str(fruits))

#part1
print("The first three items in the list are:" + str(fruits[:3]))

#part2
print("Two items from the middle of the list are:" + str(fruits[1:3]))

#part3
print("The last three items in the list are:" + str(fruits[-3:]))